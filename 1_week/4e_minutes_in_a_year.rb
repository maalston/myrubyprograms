# 4c_minutes_in_a_year.rb


=begin  Exercise 4:  Description
Write a Ruby program that tells you how many minutes there are in a year (do not 
bother right now about leap years etc.).
=end

minutes_per_hour = 60
minutes_per_day = minutes_per_hour * 24
minutes_per_year =  minutes_per_day * 365

puts "\nThere are #{minutes_per_year} minutes in a year." 